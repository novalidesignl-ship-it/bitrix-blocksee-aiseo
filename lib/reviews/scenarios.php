    /**
     * Возвращает $count сценариев без повторов (если запрошено больше, чем есть — повторяет).
     * @return string[]
     */
    public static function pickRandom(int $count): array
    {
        $count = max(1, $count);
        $all = self::ALL;
        shuffle($all);
        if ($count <= count($all)) {
            return array_slice($all, 0, $count);
        }
        $out = $all;
        while (count($out) < $count) {
            $out[] = $all[array_rand($all)];
        }
        return $out;
    }
